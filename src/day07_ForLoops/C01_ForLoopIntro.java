package day07_ForLoops;

public class C01_ForLoopIntro {
    public static void main(String[] args) {
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");

        System.out.println("------------------------");

        for (int i = 1; i <= 5;) {
            System.out.println("Hello World");
            System.out.println("i = " + i);
            i=6;
        }

    }
}

