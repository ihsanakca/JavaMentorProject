package day04_IfStatement;

public class C03_IfExamples {
    public static void main(String[] args) {
        int i=1;

        if (i==1){
            System.out.println("i = " + i);
            i++;
        }else {
            System.out.println("i = " + i);
        }

        System.out.println("i = " + i);
    }
}
